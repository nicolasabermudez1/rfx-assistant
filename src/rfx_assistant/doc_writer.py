"""Generates Centrica-branded Word documents — RFP, response templates, audit report."""
from __future__ import annotations

import json
from datetime import datetime
from pathlib import Path
from typing import Iterable

from docx import Document
from docx.enum.table import WD_ALIGN_VERTICAL
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn
from docx.oxml import OxmlElement
from docx.shared import RGBColor, Pt, Cm

from rfx_assistant.branding import NAVY, MINT, LAVENDER, PALE_LAVENDER, PURPLE
from rfx_assistant.paths import OUTPUTS


def _rgb(hex_color: str) -> RGBColor:
    return RGBColor.from_string(hex_color.lstrip("#"))


def _set_cell_shading(cell, hex_color: str):
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), hex_color.lstrip("#"))
    tc_pr.append(shd)


def _styled_doc() -> Document:
    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "Calibri"
    style.font.size = Pt(11)
    sec = doc.sections[0]
    sec.left_margin = sec.right_margin = Cm(2.0)
    sec.top_margin = sec.bottom_margin = Cm(2.0)
    return doc


def _branded_heading(doc: Document, text: str, level: int = 1, color: str = NAVY):
    p = doc.add_heading(text, level=level)
    for run in p.runs:
        run.font.name = "Arial"
        run.font.color.rgb = _rgb(color)
    return p


def _cover_block(doc: Document, title: str, subtitle: str, meta: list[tuple[str, str]]):
    p = doc.add_paragraph()
    r = p.add_run("Centrica Procurement Transformation")
    r.bold = True
    r.font.color.rgb = _rgb(PURPLE)
    r.font.size = Pt(11)
    r.font.name = "Arial"

    p = doc.add_paragraph()
    r = p.add_run(title)
    r.bold = True
    r.font.color.rgb = _rgb(NAVY)
    r.font.size = Pt(26)
    r.font.name = "Arial"

    p = doc.add_paragraph()
    r = p.add_run(subtitle)
    r.italic = True
    r.font.color.rgb = _rgb("555555")
    r.font.size = Pt(12)

    doc.add_paragraph()
    tbl = doc.add_table(rows=len(meta), cols=2)
    tbl.style = "Light Grid Accent 1"
    for i, (k, v) in enumerate(meta):
        c1 = tbl.rows[i].cells[0]
        c2 = tbl.rows[i].cells[1]
        c1.text = k
        c2.text = v
        for run in c1.paragraphs[0].runs:
            run.font.bold = True
            run.font.color.rgb = _rgb(NAVY)
        _set_cell_shading(c1, PALE_LAVENDER)
    return tbl


